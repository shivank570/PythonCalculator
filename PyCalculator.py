number1 = int(input("Choose number 1: "))
oper = input("Choose a valid operation (+, -, *, /): ")
number2 = int(input("Choose number 2: "))

if oper == "+":
       print("The answer is: " + str(number1+number2))
elif oper == "-":
       print ("The answer is: " + str(number1-number2))
elif oper == "*":
       print ("The answer is: " + str(number1*number2))
elif oper == "/":
        print ("The answer is: " + str(number1/number2))







